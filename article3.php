					<h2>Cricket</h2>
					Cricket is a kind of national sports game. The rules for it were developed almost 300 years ago.
					<h2>Football</h2>
					Football is the most popular sport in the world. The British play it in any public place, from playgrounds to parks. There are now 92 clubs in the English football league.
					<h2>Rugby</h2>
					Rugby is another interesting sport that emerged in the early nineteenth century. According to legend, it was started by one of the players, who mistakenly took the ball with his hands and ran. It happened at Rugby School - hence the name of the new game. Nowadays, not only competitions of a separate rugby league are held, but also a separate tournament, in which players from France, Italy, Scotland, Ireland, England and Wales participate.
					<h2>Golf</h2>
					Golf was originally one of the most popular traditional national games in Scotland, where there are now over four hundred golf courses. It is in Scotland that the Royal and Ancient Club is located - the most famous golf school.
					<h2>Tennis</h2>
					Tennis - it appeared at the end of the nineteenth century and is still one of the most expensive and aristocratic sports options. The largest tennis tournament, Wimbledon, takes place in England. There is one interesting fact about the tournament - it was founded in 1877 and is the oldest tennis competition, but the prize fund in monetary terms was founded in 1968.
					<h2>Polo</h2>
					Polo is another equestrian sport that appeared in England not so long ago - it got here in the 19th century. It is customary to evaluate players on the field not only in connection with their skills, but also to pay attention to the status of the family. Typically, most of the participants boast a high-level aristocratic heritage.
					<h2>Other sports</h2>
					The British also respect other sports, such as basketball, swimming, rowing, fishing and hunting, etc., many of which were initially available only to aristocrats as an interesting leisure activity. Although, despite the well-known love of competitions and sports games, due to their mentality, the people of Britain prefer not to participate, but to watch the process of the game from the side.
